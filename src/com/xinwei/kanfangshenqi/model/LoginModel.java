package com.xinwei.kanfangshenqi.model;

public class LoginModel{
	private String status;
	private String msg;
	private String memberId;
	private String account;
	private String nickName;
	private String sex;
	private String token; 
	private String headPortraitDisplay;
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getHeadPortraitDisplay() {
		return headPortraitDisplay;
	}
	public void setHeadPortraitDisplay(String headPortraitDisplay) {
		this.headPortraitDisplay = headPortraitDisplay;
	}
	
}