package com.xinwei.kanfangshenqi.model;

import java.io.Serializable;
import java.util.List;

/**
 ********************
 * 问问
 * @author cn
 ********************
 */
public class Ask implements Serializable{
	private String commentId;
	private String content;
	private String createTime;
	private String nickName;
	private String commentCount;
	private String headPortrait;
	private String type;
	private List<ImgAsk> commentImgs;
	private int replayPageCount;
	private int replayDataCount;
	private List<ReplyComment> replayComments;
	public String getCommentId() {
		return commentId;
	}
	public void setCommentId(String commentId) {
		this.commentId = commentId;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getHeadPortrait() {
		return headPortrait;
	}
	public void setHeadPortrait(String headPortrait) {
		this.headPortrait = headPortrait;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	public List<ImgAsk> getCommentImgs() {
		return commentImgs;
	}
	public void setCommentImgs(List<ImgAsk> commentImgs) {
		this.commentImgs = commentImgs;
	}
	public String getCommentCount() {
		return commentCount;
	}
	public void setCommentCount(String commentCount) {
		this.commentCount = commentCount;
	}
	
	public int getReplayPageCount() {
		return replayPageCount;
	}
	public void setReplayPageCount(int replayPageCount) {
		this.replayPageCount = replayPageCount;
	}
	public int getReplayDataCount() {
		return replayDataCount;
	}
	public void setReplayDataCount(int replayDataCount) {
		this.replayDataCount = replayDataCount;
	}
	public List<ReplyComment> getReplayComments() {
		return replayComments;
	}
	public void setReplayComments(List<ReplyComment> replayComments) {
		this.replayComments = replayComments;
	}
	@Override
	public String toString() {
		return "Ask [commentId=" + commentId + ", content=" + content
				+ ", createTime=" + createTime + ", nickName=" + nickName
				+ ", commentCount=" + commentCount + ", headPortrait="
				+ headPortrait + ", type=" + type + ", commentImgs="
				+ commentImgs + ", replayPageCount=" + replayPageCount
				+ ", replayDataCount=" + replayDataCount + ", replayComments="
				+ replayComments + "]";
	}

	/**
	 ********************
	 * 问问图片
	 * @author cn
	 ********************
	 */
	public class ImgAsk implements Serializable{
		private String commentImgId;
		private String commentImgPath;
		private String commentImgSmallPath;
		public String getCommentImgId() {
			return commentImgId;
		}
		public void setCommentImgId(String commentImgId) {
			this.commentImgId = commentImgId;
		}
		public String getCommentImgPath() {
			return commentImgPath;
		}
		public void setCommentImgPath(String commentImgPath) {
			this.commentImgPath = commentImgPath;
		}
		public String getCommentImgSmallPath() {
			return commentImgSmallPath;
		}
		public void setCommentImgSmallPath(String commentImgSmallPath) {
			this.commentImgSmallPath = commentImgSmallPath;
		}
		@Override
		public String toString() {
			return "ImgAsk [commentImgId=" + commentImgId + ", commentImgPath=" + commentImgPath
					+ ", commentImgSmallPath=" + commentImgSmallPath + "]";
		}
		
	}
}
