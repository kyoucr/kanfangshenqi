package com.xinwei.kanfangshenqi.model;

import java.util.List;
/**
 ********************
 * 问问列表
 * @author cn
 ********************
 */
public class SeeHouseCircle {
	private List<Ask> chatList;
	private List<Interest> followList;
	private List<Remind> remindList;
	private int pageCount;
	private String chatCount;//杂谈数量
	private String followCount;//关注数量
	private String remindCount;//提醒数量
	public List<Ask> getChatList() {
		return chatList;
	}

	public void setChatList(List<Ask> chatList) {
		this.chatList = chatList;
	}

	public int getPageCount() {
		return pageCount;
	}

	public void setPageCount(int pageCount) {
		this.pageCount = pageCount;
	}

	public List<Interest> getFollowList() {
		return followList;
	}

	public void setFollowList(List<Interest> followList) {
		this.followList = followList;
	}

	public List<Remind> getRemindList() {
		return remindList;
	}

	public void setRemindList(List<Remind> remindList) {
		this.remindList = remindList;
	}

	public String getChatCount() {
		return chatCount;
	}

	public void setChatCount(String chatCount) {
		this.chatCount = chatCount;
	}

	public String getFollowCount() {
		return followCount;
	}

	public void setFollowCount(String followCount) {
		this.followCount = followCount;
	}

	public String getRemindCount() {
		return remindCount;
	}

	public void setRemindCount(String remindCount) {
		this.remindCount = remindCount;
	}

	@Override
	public String toString() {
		return "SeeHouseCircle [chatList=" + chatList + ", followList="
				+ followList + ", remindList=" + remindList + ", pageCount="
				+ pageCount + ", chatCount=" + chatCount + ", followCount="
				+ followCount + ", remindCount=" + remindCount + "]";
	}

}
