package com.xinwei.kanfangshenqi.model;

import java.util.List;

public class EncyclopediaContent {
	private String classId;
	private String className;
	private String serialNum;
	private String icon;
	private List<EncyclopediaData> dataList;
	private boolean isShowAll;
	public String getClassId() {
		return classId;
	}
	public void setClassId(String classId) {
		this.classId = classId;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public List<EncyclopediaData> getDataList() {
		return dataList;
	}
	public void setDataList(List<EncyclopediaData> dataList) {
		this.dataList = dataList;
	}
	public boolean isShowAll() {
		return isShowAll;
	}
	public void setShowAll(boolean isShowAll) {
		this.isShowAll = isShowAll;
	}
	public String getSerialNum() {
		return serialNum;
	}
	public void setSerialNum(String serialNum) {
		this.serialNum = serialNum;
	}
	@Override
	public String toString() {
		return "EncyclopediaContent [classId=" + classId + ", className=" + className + ", serialNum=" + serialNum
				+ ", icon=" + icon + ", dataList=" + dataList + ", isShowAll=" + isShowAll + "]";
	}
	
}
